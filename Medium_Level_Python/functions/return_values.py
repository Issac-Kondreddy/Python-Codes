# Return Values in Python Functions

def add(a, b):
    """
    This function returns the sum of two numbers.

    Parameters:
    a (int): The first number.
    b (int): The second number.

    Returns:
    int: The sum of a and b.
    """
    return a + b


result = add(5, 7)
print(f"The sum is: {result}")

