# Numbers.py

# Integers: Whole numbers that can be positive, negative, or zero.
print("Integers:")
a = 10
b = 3

# Basic arithmetic operations with integers
print("Addition (a + b):", a + b)  # Addition
print("Subtraction (a - b):", a - b)  # Subtraction
print("Multiplication (a * b):", a * b)  # Multiplication
print("Division (a / b):", a / b)  # Division (results in a float)
print("Floor Division (a // b):", a // b)  # Floor division (results in an integer)
print("Modulus (a % b):", a % b)  # Modulus (remainder of a division)
print("Exponentiation (a ** b):", a ** b)  # Exponentiation

# Floating-point numbers: Numbers that contain a fractional part.
print("\nFloating-point Numbers:")
x = 2.5
y = 0.5

# Basic arithmetic operations with floating-point numbers
print("Addition (x + y):", x + y)  # Addition
print("Subtraction (x - y):", x - y)  # Subtraction
print("Multiplication (x * y):", x * y)  # Multiplication
print("Division (x / y):", x / y)  # Division (always results in a float)
print("Floor Division (x // y):", x // y)  # Floor division
print("Modulus (x % y):", x % y)  # Modulus
print("Exponentiation (x ** y):", x ** y)  # Exponentiation

# Note: Python automatically handles the type conversion between int and float during arithmetic operations
